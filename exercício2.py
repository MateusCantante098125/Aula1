x1 = int(input("Introduza o valor de x do ponto 1: "))
y1 = int(input("Introduza o valor de y do ponto 1: "))
x2 = int(input("Introduza o valor de x do ponto 2: "))
y2 = int(input("Introduza o valor de y do ponto 2: "))

distancia = ((x2-x1)**2 + (y2-y1)**2)**0.5
print("A distância entre os pontos é:", distancia)